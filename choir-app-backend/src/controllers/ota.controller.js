const db = require("../models");
const crypto = require("crypto");
const jwt = require("jsonwebtoken");
const logger = require("../config/logger");
const { NotFoundError, ValidationError } = require("../utils/errors");

const OneTimeToken = () => db.one_time_token;
const User = () => db.user;
const Choir = () => db.choir;

/**
 * Generate a one-time access token (admin only).
 * The token grants a session as the first admin user.
 */
exports.generate = async (req, res) => {
  const label = req.body.label || null;
  const expiresInMinutes = 60; // fixed 60 min

  // Find the first admin user (works with both PostgreSQL and SQLite)
  const allUsers = await User().findAll({
    include: [{ model: Choir(), attributes: ['id', 'name'] }],
    order: [['id', 'ASC']],
  });
  const adminUser = allUsers.find(u => Array.isArray(u.roles) && u.roles.includes('admin'));

  if (!adminUser) {
    throw new NotFoundError('Kein Admin-Benutzer gefunden.');
  }

  const token = crypto.randomBytes(32).toString('hex');
  const expiresAt = new Date(Date.now() + expiresInMinutes * 60 * 1000);

  const record = await OneTimeToken().create({
    token,
    createdByUserId: req.userId,
    targetUserId: adminUser.id,
    expiresAt,
    label,
  });

  logger.info(`OTA token generated by user ${req.userId} for admin user ${adminUser.id}, expires ${expiresAt.toISOString()}`);

  res.status(201).send({
    token: record.token,
    expiresAt: record.expiresAt,
    targetUser: `${adminUser.firstName} ${adminUser.name}`,
    label: record.label,
  });
};

/**
 * Consume a one-time access token (public endpoint).
 * Returns a JWT session for the target admin user. Token is invalidated after use.
 */
exports.consume = async (req, res) => {
  const { token } = req.params;

  if (!token || token.length !== 64) {
    throw new ValidationError('Ungültiger Token.');
  }

  const record = await OneTimeToken().findOne({ where: { token } });

  if (!record) {
    logger.warn(`OTA token consumption failed: token not found`);
    throw new NotFoundError('Token nicht gefunden oder bereits verwendet.');
  }

  if (record.usedAt) {
    logger.warn(`OTA token consumption failed: already used at ${record.usedAt}`);
    throw new ValidationError('Dieser Token wurde bereits verwendet.');
  }

  if (new Date() > record.expiresAt) {
    logger.warn(`OTA token consumption failed: expired at ${record.expiresAt}`);
    throw new ValidationError('Dieser Token ist abgelaufen.');
  }

  // Mark as used
  record.usedAt = new Date();
  record.usedByIp = req.ip;
  await record.save();

  // Load the target user with choirs
  const user = await User().findByPk(record.targetUserId, {
    include: [{ model: Choir(), attributes: ['id', 'name'] }],
  });

  if (!user || !user.choirs || user.choirs.length === 0) {
    throw new NotFoundError('Zielbenutzer nicht verfügbar.');
  }

  const activeChoirId = user.choirs[0].id;

  const jwtToken = jwt.sign(
    { id: user.id, activeChoirId, roles: user.roles },
    process.env.JWT_SECRET,
    { expiresIn: '8h' }
  );

  logger.info(`OTA token consumed for user ${user.id} from IP ${req.ip}`);

  const isProduction = process.env.NODE_ENV === 'production';
  res.cookie('auth-token', jwtToken, {
    httpOnly: true,
    secure: isProduction,
    sameSite: isProduction ? 'strict' : 'lax',
    maxAge: 8 * 60 * 60 * 1000,
    path: '/',
  });

  res.status(200).send({
    id: user.id,
    firstName: user.firstName,
    name: user.name,
    email: user.email,
    roles: user.roles,
    accessToken: jwtToken,
    activeChoir: user.choirs[0],
    availableChoirs: user.choirs,
  });
};

/**
 * List all OTA tokens (admin only).
 */
exports.list = async (req, res) => {
  const tokens = await OneTimeToken().findAll({
    order: [['createdAt', 'DESC']],
    include: [
      { model: User(), as: 'createdBy', attributes: ['id', 'firstName', 'name'] },
      { model: User(), as: 'targetUser', attributes: ['id', 'firstName', 'name'] },
    ],
  });
  res.send(tokens);
};

/**
 * Revoke (delete) an unused OTA token (admin only).
 */
exports.revoke = async (req, res) => {
  const { id } = req.params;
  const record = await OneTimeToken().findByPk(id);

  if (!record) {
    throw new NotFoundError('Token nicht gefunden.');
  }

  if (record.usedAt) {
    throw new ValidationError('Bereits verwendete Tokens können nicht widerrufen werden.');
  }

  await record.destroy();
  logger.info(`OTA token ${id} revoked by user ${req.userId}`);
  res.send({ message: 'Token widerrufen.' });
};
